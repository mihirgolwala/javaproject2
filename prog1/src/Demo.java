public class Demo {
    public static void main(String[] args) {
        int a=5,b=6;
        char op='+';
        switch(op){
            case '+':
                System.out.println("Addition is "+(a+b));
                break;
            case '-':
                System.out.println("Subtraction is "+(a-b));
                break;
        }
    }


}
